// Helper to get pins by category
export const getPinsByCategory = (category: string): PinItem[] => {
  return PINS_LIBRARY.filter(pin => pin.category === category);
};

// Helper to get all categories with their pin counts
export const getCategoriesWithCounts = (): Array<{ category: string; count: number }> => {
  return PIN_CATEGORIES.map(category => ({
    category,
    count: getPinsByCategory(category).length
  }));
};
